"""Supabase `books` 행에 넣기 전 문자열 상한 (클라이언트 측 방어만).

PostgreSQL `text` 컬럼은 실질적으로 길이 제한이 없으나, API·파싱 버그로 비정상적으로
큰 문자열이 들어오는 경우 메모리·요청 크기를 보호하기 위해 **잘라서** 저장한다.

**알라딘 TTB ItemLookUp** (`ItemLookUp.aspx`, `Version=20131101` 등) 공개 문서에는
`description`·`cover`·`pubDate` 등 필드별 **최대 문자 수가 명시되어 있지 않다.**
따라서 아래 숫자는 “알라딘이 실제로 줄 수 있는 일반적인 도서 메타”를 넘는 여유를 두되,
명백한 쓰레기/오류 응답만 걸러내는 **보수적으로 큰 상한**이다.

- 정보나루 `srchDtlList`에서 오는 `class_no` / `class_nm` 은 알라딘이 아니지만,
  동일한 `row_for_db` 경로로 들어가므로 같은 모듈에서 길이를 맞춘다.

요약:
    - 짧은 필드(제목·저자·출판사·KDC·연도·표지 URL): 실무에서 거의 안 잘리게 충분히 크게.
    - 긴 필드(책 소개·편집자/서평 텍스트): 수백만 자 단위까지 허용(일반 도서 소개는 여기 안에서 끝남).
"""

from __future__ import annotations

# ---------------------------------------------------------------------------
# 알라딘 ItemLookUp JSON — 본문에 가까운 필드
# ---------------------------------------------------------------------------
# `description`, `editorial_review` 합성( reviewList 등 )은 도서당 수만~수십만 자까지
# 갈 수 있으나, 50_000자에서 자르던 기존 설정은 긴 항목에서 **의도치 않은 손실**이 났다.
# 1_048_576 = 2**20 자(약 1Mi 문자) — UTF-8로는 최대 약 4MiB까지, 비정상 페이로드만 차단.
MAX_CHARS_ALADIN_LONG_TEXT: int = 1_048_576

# ---------------------------------------------------------------------------
# 알라딘 — title, author, authorInfo(저자 소개) 등 일반 텍스트
# ---------------------------------------------------------------------------
# 표제·저자 문자열이 극단적으로 긴 경우(학술 도서 등)에도 잘리지 않게 256Ki 자.
MAX_CHARS_ALADIN_MEDIUM_TEXT: int = 262_144

# ---------------------------------------------------------------------------
# 표지 URL — `cover` 또는 `coverUrl` (문자열 또는 객체 내 url)
# ---------------------------------------------------------------------------
# 실제 서비스 URL은 대부분 2KB 미만이나, CDN 서명·쿼리가 길어질 수 있다.
# 64Ki 문자는 WHATWG URL 실무 상한을 넘는 수준으로, **오류로 수 MB짜리 문자열이 온 경우**만 걸러낸다.
MAX_CHARS_ALADIN_COVER_URL: int = 65_536

# ---------------------------------------------------------------------------
# 알라딘 pubDate — 응답은 보통 "2003-11-15" 형태. 이상하게 긴 문자열 방어만.
# ---------------------------------------------------------------------------
MAX_CHARS_ALADIN_PUB_DATE_RAW: int = 64

# ---------------------------------------------------------------------------
# DB `published_year` 컬럼 — 실제로는 연도 4자리만 넣지만 JSON 재시드 호환으로 여유 유지
# ---------------------------------------------------------------------------
MAX_CHARS_PUBLISHED_YEAR: int = 32

# ---------------------------------------------------------------------------
# 출판사 — 알라딘·정보나루 중 긴 쪽 (합명·부제 포함 시)
# ---------------------------------------------------------------------------
MAX_CHARS_PUBLISHER: int = 8_192

# ---------------------------------------------------------------------------
# 정보나루 KDC — `class_no` 짧은 코드 / `class_nm` 은 `>` 로 이어진 전체 경로가 길 수 있음
# ---------------------------------------------------------------------------
MAX_CHARS_KDC_CLASS_NO: int = 256
MAX_CHARS_KDC_CLASS_NM: int = 16_384


def clip(s: str | None, max_len: int) -> str:
    """문자열을 max_len 자로 제한. None 은 빈 문자열.

    max_len 은 위 상수 중 하나를 쓰는 것을 권장한다.
    """
    if s is None:
        return ""
    out = str(s)
    if len(out) <= max_len:
        return out
    return out[:max_len]
